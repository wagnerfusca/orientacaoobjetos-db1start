package br.com.db1.db1start.aula10;

public class MinhaPrimeiraClasse {

	public static Integer somar(Integer numeroUm, Integer numeroDois) {
		return numeroUm + numeroDois;
	}

	public static String imprimirValor(Integer valor) {
		return "" + valor;
	}

	public static Double divisao(Double numeroUm, Double numeroDois) {
		return numeroUm / numeroDois;
	}
}